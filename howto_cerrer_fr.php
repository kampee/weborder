<font face='arial' size="3"><strong>Bienvenue à CLW Syste'me de commande en ligne.</strong><br></font>
<font face="arial" size="2">
Pour vous aider à passer une commande plus facilement, CLW a mis au point ce Système de commande en ligne. En suivant les étapes suivantes, très faciles, vous pourrez passer votre commande en ligne rapidement et sans souci.
<br><br>

<u>Pour mettre des produits dans votre panier</u><br>
1.	Dans la page présentant les produits, vous pouvez cocher les produits à commander en appuyant sur le carré à coté de l’image des produits de votre choix.<br>
2.	Indiquer la quantité désirée de chaque produit.<br>
3.	Appuyer sur le bouton « AJOUTER AU PANIER ». Ceci vous amènera automatiquement à la page VOIR LE PANIER où vous pourrez le montant actualisé de votre commande.<br>
<br>

<u>Vérifier, modifier et confirmer la liste de produits dans votre panier.</u><br>
1.	Vérifier attentivement et en détails la liste de produits dans votre panier. Si tout est correct, placer la commande. en appuyant sur le bouton « COMMANDER MAINTENANT ». Autrement, enlever ou modifier des articles.<br>
2.	Pour enlever des articles, sélectionner  le carré approprié et presser le bouton EFFACER.<br>
3.	Pour mettre à jour ou modifier des informations comme la quantité, sélectionner le carré approprié, modifier et presser le bouton « Mettre à jour »<br>
<br>

<u>Remplir le bon de commande</u><br>
1.	Cette page contient des renseignements sur différents sujets comme le mode de paiement, la facturation, la demande de catalogues, etc.<br>
<br>

<u>Confirmer le paiement</u><br>
1.	Cette page servira à confirmer votre paiement par carte de crédit ou par transfert T/T.<br>
2.	Si vous payez par carte de crédit, inscrire les renseignements requis concernant votre carte de crédit puis presser le bouton « SOUMETTRE »<br>
3.	Si vous payez par transfert T/T, vérifier simplement les renseignements fournis puis presser le bouton « SOUMETTRE »<br>
<br>

<u>Commande transmise avec succès</u><br>
1.	Votre commande a été transmise avec succès à CLW si vous voyez cette page apparaître à l’écran<br>
2.	Vous verrez également sur la même page le numéro de commande que vous devez noter pour faire le suivi ou des consultations.<br>
<br>

Après avoir complété votre commande, un message de confirmation vous sera envoyé  à l’adresse correspondant à votre identification dans le Système.
Une personne de notre Service à la clientèle entrera en contact avec vous dans les 24 heures  
<br>
<br>

<b>Autres fonctionalités:</b><br>
Zone de recherche Produit Réf. Pour faire la recherche d’un produit en utilisant son numéro de référence.<br>
Zone de recherche Suivre la commande: Pour connaître le statut de votre commande en utilisant le numéro de la commande pour faire la recherche.<br>
Suivre la commande. Pour voir toutes les commandes que vous nous avez passées précédemment.<br>
Quitter: Pour se déconnecter du Système.<br>
<br>

